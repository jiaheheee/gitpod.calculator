#include<stdio.h>
#include<stdlib.h>
char main(){
    float a,b,c,d,e,f;
    char A;
    scanf("%c",&A);
    printf("%c\n",A);
    if ("A==Z") {
        printf("加法\n");
        }
    if ("A==Z"){
        scanf("%f %f",&a,&b);
        }
    c=a+b;
    printf("%f+%f=%f\n",a,b,c);
    return 0;
    
    if ("A==Y"){
        printf("减法\n");
        }
    if ("A==Y"){
        scanf("%f %f",&d,&e);
        }
    f=d-e;
    printf("%f-%f=%f\n",d,e,f);
    return 0;
}